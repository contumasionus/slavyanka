# Находим все параграфы документа
# Удаляем старые параграфы от "ЗАКЛЮЧЕНИЕ" до "СПИСОК ЛИТЕРАТУРЫ"
# и вставляем новые

# Сначала найдем индексы
start_idx = -1
end_idx = -1

for i, p in enumerate(doc.paragraphs):
    text = p.text.strip().upper()
    if 'ЗАКЛЮЧЕНИЕ' in text and len(text) < 30:
        start_idx = i
    if start_idx != -1 and 'СПИСОК ЛИТЕРАТУРЫ' in text:
        end_idx = i
        break

if start_idx != -1 and end_idx != -1:
    # Удаляем старые параграфы (с конца, чтобы не сбить индексы)
    for i in range(end_idx - 1, start_idx - 1, -1):
        p = doc.paragraphs[i]
        p._element.getparent().remove(p._element)

    # Вставляем новые параграфы после start_idx
    lines = new_conclusion.strip().split('\n')
    
    # Находим элемент, после которого будем вставлять
    # start_idx - это параграф с "ЗАКЛЮЧЕНИЕ" (заголовок), его оставляем
    # Новый текст будем вставлять после него
    
    ref_element = doc.paragraphs[start_idx]._element
    
    for line in lines:
        line = line.strip()
        if not line:
            continue
        
        # Определяем, является ли строка маркированным списком (начинается с "– ")
        is_bullet = line.startswith('– ')
        
        from docx.oxml import OxmlElement
        
        new_p = OxmlElement('w:p')
        
        # Настройки абзаца
        pPr = OxmlElement('w:pPr')
        
        # Выравнивание (justify)
        jc = OxmlElement('w:jc')
        jc.set(qn('w:val'), 'both')
        pPr.append(jc)
        
        # Межстрочный интервал 1.5
        spacing = OxmlElement('w:spacing')
        spacing.set(qn('w:line'), '360')
        spacing.set(qn('w:lineRule'), 'auto')
        spacing.set(qn('w:after'), '0')
        spacing.set(qn('w:before'), '0')
        pPr.append(spacing)
        
        # Отступ первой строки (кроме заголовка и списка)
        if not is_bullet and line != 'ЗАКЛЮЧЕНИЕ':
            ind = OxmlElement('w:ind')
            ind.set(qn('w:firstLine'), '450')  # ~1.25 см в twips
            pPr.append(ind)
        
        new_p.append(pPr)
        
        # Run
        r = OxmlElement('w:r')
        rPr = OxmlElement('w:rPr')
        
        rFonts = OxmlElement('w:rFonts')
        rFonts.set(qn('w:ascii'), 'Times New Roman')
        rFonts.set(qn('w:hAnsi'), 'Times New Roman')
        rFonts.set(qn('w:cs'), 'Times New Roman')
        rPr.append(rFonts)
        
        sz = OxmlElement('w:sz')
        sz.set(qn('w:val'), '28')  # 14pt = 28 half-pt
        rPr.append(sz)
        
        szCs = OxmlElement('w:szCs')
        szCs.set(qn('w:val'), '28')
        rPr.append(szCs)
        
        if line == 'ЗАКЛЮЧЕНИЕ':
            # Заголовок - жирный, по центру
            b = OxmlElement('w:b')
            rPr.append(b)
            jc.set(qn('w:val'), 'center')
            spacing.set(qn('w:after'), '200')
        
        r.append(rPr)
        
        t = OxmlElement('w:t')
        t.text = line
        t.set(qn('xml:space'), 'preserve')
        r.append(t)
        
        new_p.append(r)
        
        # Вставляем после ref_element
        ref_element.addnext(new_p)
        ref_element = new_p

doc.save(dst)
print(f'Файл сохранён: {dst}')