async function seed() {
  console.log('Добавляем демо-новости...');

  for (const item of demoNews) {
    const existing = await prisma.news.findUnique({ where: { slug: item.slug } });
    if (existing) {
      console.log('  Пропускаем "' + item.title + '" — уже существует');
      continue;
    }
    await prisma.news.create({ data: item });
    console.log('  Добавлена: "' + item.title + '"');
  }

  console.log('\nГотово!');
  await prisma.$disconnect();
}

seed().catch((e) => {
  console.error('Ошибка:', e);
  prisma.$disconnect();
  process.exit(1);
});